// The reverse method is designed to do just that -- reverse the orser of the given array
// The reverse method does not change or mutate the original array
// Check out some examples below...
let arr = ["shannon", "princess", "beast", "bruno"];

// If want to reverse the order of the array we simply call the reverse method like so...
console.log(arr.reverse()); // return [ 'bruno', 'beast', 'princess', 'shannon' ]
